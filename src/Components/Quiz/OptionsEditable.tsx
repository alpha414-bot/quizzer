import Input from "@/Components/Input";
import { QuizOptionType } from "@/Types/Module";
import { Control } from "react-hook-form";

const OptionEditable: React.FC<{
  option: QuizOptionType;
  control: Control;
  name: string;
  answer?: { setAnswer: any; value?: string | null };
  updateOption: any;
  removeOption: any;
}> = ({ name, option, updateOption, removeOption, control, answer }) => {
  const right_option =
    answer?.value?.toLowerCase() === option?.key?.toLowerCase();
  return (
    <div className="flex items-center gap-2" data-key={name}>
      <div
        data-key={option.key}
        className="grow flex items-center gap-1 cursor-text"
      >
        <button
          onClick={() => {
            removeOption(option.key);
          }}
        >
          <svg
            className="w-7 h-7 text-red-600"
            aria-hidden="true"
            xmlns="http://www.w3.org/2000/svg"
            width="24"
            height="24"
            fill="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              fillRule="evenodd"
              d="M2 12C2 6.477 6.477 2 12 2s10 4.477 10 10-4.477 10-10 10S2 17.523 2 12Zm5.757-1a1 1 0 1 0 0 2h8.486a1 1 0 1 0 0-2H7.757Z"
              clipRule="evenodd"
            />
          </svg>
        </button>
        <Input
          key={`${name}`}
          name={`${name}`}
          control={control}
          defaultValue={option.value}
          placeholder="Enter value"
          floating={false}
          beforeOnChange={(value: any) => {
            updateOption(option.key, value);
          }}
        />
      </div>
      <button
        onClick={() => {
          answer?.setAnswer(option.key);
        }}
        className="border border-green-600 border-dashed rounded-full"
      >
        {(right_option && (
          <svg
            className="w-8 h-8 text-green-500"
            aria-hidden="true"
            xmlns="http://www.w3.org/2000/svg"
            width="24"
            height="24"
            fill="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              fillRule="evenodd"
              d="M12 2c-.791 0-1.55.314-2.11.874l-.893.893a.985.985 0 0 1-.696.288H7.04A2.984 2.984 0 0 0 4.055 7.04v1.262a.986.986 0 0 1-.288.696l-.893.893a2.984 2.984 0 0 0 0 4.22l.893.893a.985.985 0 0 1 .288.696v1.262a2.984 2.984 0 0 0 2.984 2.984h1.262c.261 0 .512.104.696.288l.893.893a2.984 2.984 0 0 0 4.22 0l.893-.893a.985.985 0 0 1 .696-.288h1.262a2.984 2.984 0 0 0 2.984-2.984V15.7c0-.261.104-.512.288-.696l.893-.893a2.984 2.984 0 0 0 0-4.22l-.893-.893a.985.985 0 0 1-.288-.696V7.04a2.984 2.984 0 0 0-2.984-2.984h-1.262a.985.985 0 0 1-.696-.288l-.893-.893A2.984 2.984 0 0 0 12 2Zm3.683 7.73a1 1 0 1 0-1.414-1.413l-4.253 4.253-1.277-1.277a1 1 0 0 0-1.415 1.414l1.985 1.984a1 1 0 0 0 1.414 0l4.96-4.96Z"
              clipRule="evenodd"
            />
          </svg>
        )) || (
          <svg
            className="w-8 h-8 text-gray-800"
            aria-hidden="true"
            xmlns="http://www.w3.org/2000/svg"
            width="24"
            height="24"
            fill="none"
            viewBox="0 0 24 24"
          >
            <path
              stroke="currentColor"
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth="2"
              d="m8.032 12 1.984 1.984 4.96-4.96m4.55 5.272.893-.893a1.984 1.984 0 0 0 0-2.806l-.893-.893a1.984 1.984 0 0 1-.581-1.403V7.04a1.984 1.984 0 0 0-1.984-1.984h-1.262a1.983 1.983 0 0 1-1.403-.581l-.893-.893a1.984 1.984 0 0 0-2.806 0l-.893.893a1.984 1.984 0 0 1-1.403.581H7.04A1.984 1.984 0 0 0 5.055 7.04v1.262c0 .527-.209 1.031-.581 1.403l-.893.893a1.984 1.984 0 0 0 0 2.806l.893.893c.372.372.581.876.581 1.403v1.262a1.984 1.984 0 0 0 1.984 1.984h1.262c.527 0 1.031.209 1.403.581l.893.893a1.984 1.984 0 0 0 2.806 0l.893-.893a1.985 1.985 0 0 1 1.403-.581h1.262a1.984 1.984 0 0 0 1.984-1.984V15.7c0-.527.209-1.031.581-1.403Z"
            />
          </svg>
        )}
      </button>
    </div>
  );
};

export default OptionEditable;
